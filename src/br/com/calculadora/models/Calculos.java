package br.com.calculadora.models;

public class Calculos {

	public int adicao(int a, int b) {
		return a + b;
	}

	public int subtracao(int a, int b) {
		return a - b;

	}

	public double divisao(double a, double b) {
		return a / b;

	}

	public double multiplicacao(int a, int b) {
		return a * b;

	}

}
